import { readFile, readdir, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const packageRoot = join(here, '..');
const iconJsonDir = join(packageRoot, 'node_modules', '@iconify', 'json', 'json');
const outputPath = join(packageRoot, 'src', 'preview', 'iconIndex.generated.ts');

const files = (await readdir(iconJsonDir)).filter((file) => file.endsWith('.json')).sort();
const index = {};

for (const file of files) {
  const collection = JSON.parse(await readFile(join(iconJsonDir, file), 'utf8'));
  if (collection.prefix && collection.icons) {
    index[collection.prefix] = Object.keys(collection.icons).sort();
  }
}

const generated = `// Generated by scripts/generate-icon-index.mjs. Do not edit by hand.\n` +
  `export const iconSearchIndex: Record<string, readonly string[]> = ${JSON.stringify(index)};\n`;

await writeFile(outputPath, generated);
console.log(`Generated ${outputPath} with ${Object.values(index).reduce((sum, names) => sum + names.length, 0)} icons.`);
