import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import {
  compileThemePaletteFile,
  discoverThemePaletteFiles,
} from '../src/renderer/themes/loader.ts';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'src/renderer/themes/generated');
mkdirSync(outDir, { recursive: true });
const outFile = join(outDir, 'builtinThemes.ts');

const themes = discoverThemePaletteFiles().map((file) => ({
  id: file.id,
  label: file.label,
  appearance: file.appearance,
  tokens: compileThemePaletteFile(file),
}));

const source = `/* Auto-generated by scripts/compile-builtin-themes.mjs — do not edit. */
import type { DiagramThemeDefinition } from '../registry.js';

export const BUILTIN_THEME_DEFINITIONS = ${JSON.stringify(themes, null, 2)} as DiagramThemeDefinition[];
`;

writeFileSync(outFile, source);
console.log(`Generated ${themes.length} built-in themes → ${outFile}`);
